package com.wucstone.sort;



public class Node {
	/**
	 * @author: 行者摩罗
	 */
	private int data;
	private Node next;
 
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public Node getNext() {
		return next;
	}
	public void setNext(Node next) {
		this.next = next;
	}
}